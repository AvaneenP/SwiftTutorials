// var creats a variable
// let creates a constant

let maleName = "Wayne"

// swift will recommend you change variables to constants if it is noticed that the value does not change
// constants allow your code to be more safe


// constants do not require values at compile time - they can be calculated at runtime
// allows for type annotation of constants

let deathAge: Int
let birthYear: Int
let birthWeight: Double

// these constants must be initialized before they are used
deathAge = 76
birthYear = 2002
birthWeight = 7.67
